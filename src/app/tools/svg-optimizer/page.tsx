function optimizeSVG(svg: string): string {
  let result = svg;

  // Remove XML declaration
  result = result.replace(/<\?xml[^?]*\?>\s*/gi, "");

  // Remove comments
  result = result.replace(/<!--[\s\S]*?-->/g, "");

  // Remove DOCTYPE
  result = result.replace(/<!DOCTYPE[^>]*>/gi, "");

  // Remove metadata elements
  result = result.replace(/<metadata[\s\S]*?<\/metadata>/gi, "");
  result = result.replace(/<desc[\s\S]*?<\/desc>/gi, "");
  result = result.replace(/<title[\s\S]*?<\/title>/gi, "");

  // Remove common editor-specific attributes
  const editorAttrs = [
    /\s+xmlns:xlink="[^"]*"/g,
    /\s+xmlns:dc="[^"]*"/g,
    /\s+xmlns:cc="[^"]*"/g,
    /\s+xmlns:rdf="[^"]*"/g,
    /\s+xmlns:svg="[^"]*"/g,
    /\s+xmlns:sodipodi="[^"]*"/g,
    /\s+xmlns:inkscape="[^"]*"/g,
    /\s+sodipodi:[a-z-]+=["'][^"']*["']/g,
    /\s+inkscape:[a-z-]+=["'][^"']*["']/g,
    /\s+sketch:[a-z-]+=["'][^"']*["']/g,
    /\s+data-name="[^"]*"/g,
  ];
  for (const attr of editorAttrs) {
    result = result.replace(attr, "");
  }

  // Remove empty attributes (e.g., fill="" stroke="")
  result = result.replace(/\s+[a-z-]+=["']\s*["']/g, "");

  // Trim decimal numbers to max 2 decimal places
  result = result.replace(/(\d+\.\d{3,})/g, (match) =>
    parseFloat(match).toFixed(2)
  );

  // Collapse multiple whitespace into single space
  result = result.replace(/\s{2,}/g, " ");

  // Remove whitespace between tags
  result = result.replace(/>\s+</g, "><");

  // Trim
  result = result.trim();

  return result;
}

